<template>
  <Mikan ref="mikan" @add="args => {
    ani.url = args.url
    ani.match = args.match
    getRss()
  }"/>
  <Bgm ref="bgmRef" @add="it => {
    ani.title = it['name_cn']
    ani.bgmUrl = it.url
  }"/>
  <el-dialog v-model="dialogVisible" title="添加订阅" center v-if="dialogVisible">
    <div v-if="showRss">
      <el-tabs tab-position="left" v-model="activeName">
        <el-tab-pane label="Mikan" name="mikan">
          <el-form label-width="auto"
                   style="height: 260px"
                   v-if="showRss"
                   @submit="(event)=>{
                event.preventDefault()
             }">
            <el-form-item label="RSS 地址">
              <div style="width: 100%">
                <el-input
                    :disabled="rssButtonLoading"
                    type="textarea"
                    :autosize="{ minRows: 2}"
                    v-model:model-value="ani.url"
                    placeholder="https://mikanani.me/RSS/Bangumi?bangumiId=xxx&subgroupid=xxx"
                />
                <br>
                <div style="width: 100%;display: flex;justify-content: end;margin-top: 8px;">
                  <el-button @click="mikan?.show()" text bg icon="VideoCamera" type="primary"
                             :disabled="rssButtonLoading">Mikan
                  </el-button>
                </div>
                <div>
                  <el-text class="mx-1" size="small">
                    不支持聚合订阅，原因是如果一次过多更新会出现遗漏
                    <br>
                    不必在 mikan 网站添加订阅, 你可以通过上方👆 [Mikan] 按钮浏览字幕组订阅
                  </el-text>
                </div>
              </div>
            </el-form-item>
          </el-form>
        </el-tab-pane>
        <el-tab-pane label="Other" name="other">
          <el-form label-width="auto"
                   style="height: 200px"
                   v-if="showRss"
                   @submit="(event)=>{
                event.preventDefault()
             }">
            <el-form-item label="番剧名称">
              <div style="display: flex;width: 100%;">
                <div style="flex: 1">
                  <el-input
                      :disabled="rssButtonLoading"
                      v-model:model-value="ani.title"
                      placeholder="可以为空 如果获取失败建议补全"
                  />
                </div>
                <div style="width: 4px;"></div>
                <el-button text bg icon="Search" @click="bgmRef?.show(ani.title)" :disabled="rssButtonLoading"/>
              </div>
            </el-form-item>
            <el-form-item label="BgmUrl">
              <el-input
                  v-model:model-value="ani.bgmUrl"
                  placeholder="https://bgm.tv/subject/123456"
                  :disabled="rssButtonLoading"
              />
            </el-form-item>
            <el-form-item label="RSS 地址">
              <el-input
                  :disabled="rssButtonLoading"
                  :autosize="{ minRows: 2}"
                  type="textarea"
                  v-model:model-value="ani.url"
                  placeholder="https://xxxx.com/a.xml"
              />
            </el-form-item>
          </el-form>
          <el-text class="mx-1" size="small">
            dmhy等含有磁力链接的RSS不支持Aria2
          </el-text>
        </el-tab-pane>
      </el-tabs>
      <div style="display: flex;justify-content: end;width: 100%;margin-top: 10px;">
        <el-button :loading="rssButtonLoading" @click="getRss" text bg icon="Check">确定</el-button>
      </div>
    </div>
    <div v-else>
      <Ani v-model:ani="ani" @ok="addAni" @next="nextAni"/>
    </div>
  </el-dialog>
</template>

<script setup>
import {ref} from "vue";
import {ElMessage} from "element-plus";
import api from "../api.js";
import Mikan from "./Mikan.vue";
import Ani from "./Ani.vue";
import Bgm from "./Bgm.vue";

const showRss = ref(true)
const mikan = ref()
const bgmRef = ref()

const dialogVisible = ref(false)

const ani = ref({
  'url': '',
  'season': 1,
  'offset': 0,
  'title': '',
  'themoviedbName': '',
  'exclude': [],
  'enable': true,
  'ova': false,
  'totalEpisodeNumber': '',
  'customDownloadPath': false,
  'downloadPath': '',
  'year': 1970,
  'month': 1,
  'day': 1,
  'week': 1,
  'subgroup': '',
  'backRssList': [],
  'bgmUrl': '',
  'customEpisode': false,
  'customEpisodeStr': '',
  'customEpisodeGroupIndex': 0
})

const rssButtonLoading = ref(false)

const getRss = () => {
  rssButtonLoading.value = true
  ani.value.type = activeName.value
  api.post('api/rss', ani.value)
      .then(res => {
        let match = ani.value['match'];
        ani.value = res['data']
        ani.value['match'] = match
        ani.value.showDownlaod = false
        showRss.value = false
      })
      .finally(() => {
        rssButtonLoading.value = false
      })
}

const addAni = (fun) => {
  api.post('api/ani', ani.value)
      .then(res => {
        ElMessage.success(res.message)
        emit('load')
        dialogVisible.value = false
      }).finally(fun)
}

const nextAni = (fun) => {
  api.post('api/ani', ani.value)
      .then(res => {
        ElMessage.success(res.message)
        emit('load')
        dialogVisible.value = false
        show()
      }).finally(fun)
}

const activeName = ref('mikan')

const show = () => {
  ani.value = {
    'url': '',
    'season': 1,
    'offset': 0,
    'title': '',
    'exclude': [],
    'totalEpisodeNumber': 0,
    'match': []
  }
  activeName.value = 'mikan'
  showRss.value = true
  dialogVisible.value = true
  rssButtonLoading.value = false
}

defineExpose({show})
const emit = defineEmits(['load'])

</script>

